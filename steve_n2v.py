def k_O_N2v__NO_N_armenise(v, T):
    """
    Sum over all NO(w) product levels of:
    O + N2(v) --> NO(w) + N
    using Armenise (2023, 2024) rates.
    Useful in cases where vib levels of NO are not resolved.
    """
    # w runs only over bound levels (where Ew < 0)
    valid_w = [w for w in range(48) if ENO[w] < 0]
    return sum(k_O_N2v__NOw_N_armenise(v, w, T) for w in valid_w)


def k_O_N2v__NO_N_dmitrieva(v, T):
    """
    Rate coefficient for 
    O + N2(v) --> NO + N
    using Dmitrieva and Zenevich (1984) rates,
    which are used in Pavlov (2011)
    """
    A = np.array([-10.553, -10.761, -10.546, -10.496, -10.413, -10.299, -10.211, -10.099, -9.9925, -9.9557, -9.9303])
    B = np.array([-3.5785, -2.1786, -2.0185, -1.3926, -1.0020, -0.79517, -0.55626, -0.47656, -0.47058, -0.26060, -0.088046])
    C = np.array([0.10993, 0.065508, 0.63923, 0.49675, 0.35965, 0.29601, 0.16353, 0.15306, 0.20699, 0.072218, -0.057985])
    D = np.array([0,       0,        -0.086991, -0.068906, -0.050763, -0.043939, -0.021204, -0.023201, -0.038499, -0.014709, 0.010343])
    
    if v<10:
        return 0.0
        
    elif v<=21:
        i = v-10
        return 10**(A[i] + B[i]*(1000/T) + C[i]*(1000/T)**2 + D[i]*(1000/T)**3)
        
    else: # Assume it's equal to the v=21 rate
        return k_O_N2v__NO_N_dmitrieva(21, T)



def calculate_rate_coefficients(EEDF_eV,EEDF,delta_function=0,electronic_vib_resolution=0,NO_vib=1,laporta=1):
    '''
    Calculates rate coefficients for production of various vibrationally and electronically excited states via electronic impact excitation 
    
    Inputs:
    
        EEDF_eV - array of energies on which the EEDF is defined in eV. NOTE: for a delta_function, this should be a single value
        EEDF - array of EEDF values (units of probability/eV). Should integrate to 1. -> NOTE: NOT THE SAME AS THE EEDF RETURNED BY BOLSIG+, WHICH HAS UNITS OF EV^-3/2
        delta_function - set to 1 if the EEDF is a delta function. In this case, specify EEDF=inf and let EEDF_eV be the energy at which the delta function occurs
        electronic_vib_resolution - some of the N2 triplet states have cross sections that have additional vibrational state resolution. If this flag is set to 0 (default), that is ignored.
        NO_vib - set to 1 if you want to calculate rates for NO vibrational states
        laporta - set to 1 if you want to use the Laporta et al. (2012) N2 vibrational cross sections
    
    Returns:
    
        rate_coefficients - xarray with rate coefficients in cm^3/s
    '''
    if laporta==0:
        raise Exception('Not supported yet')

    #load relevant cross sections (Update: Assume this has already been loaded)
    # if laporta:
    #     cross_sections = xr.open_dataset('./cross_sections_N2_laporta.nc') #CHANGE THIS FILEPATH AS NEEDED!
    # else:
    #     cross_sections = xr.open_dataset('./cross_sections_with_NO_vib.nc') #CHANGE THIS FILEPATH AS NEEDED!
    xsec_eV = cross_sections["eV"]

    #first, do the vibrational states of N2:
    if laporta:
        N2_da = cross_sections["N2_vib_cross_section"]
        vstart = cross_sections["vib_state_start"].values
        vend   = cross_sections["vib_state_end"].values
        k_N2_vib = np.full((vstart.size, vend.size), np.nan, dtype=float)

        # integrate per (vin, vout)
        for i, vin in enumerate(vstart):
            for j, vout in enumerate(vend):
                xsec_cm2 = N2_da.isel(vib_state_start=i, vib_state_end=j).values  # on xsec_eV grid
