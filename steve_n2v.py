def reaction_input_output(df, add_emissions=[]):
    '''
    Unlike previous version, this relies on the user to specify how many v levels by which were included
    in the dataframe.
    
    add_emissions = A list of emission strings to manually add, not included in df. e.g., ['hvN21P','hvcont']
    
    Parse the raw reactions list and return some useful stuff:
    species   - list(str) - all of the species to track
    emissions - list(str) - all of the emissions to track
    din   - dict - a list of reactants for each reaction
    dout  - dict - a list of products for each reaction
    demis - dict - a list of emissions for each reaction
    sidx  - dict - a dict mapping species names to their index 
    '''

    cols = []
    for i in df.index:
        s = df.loc[i, 'Reaction']
        inout = s.split(' → ')
        ins = inout[0].split(' + ')
        outs = inout[1].split(' + ')
        ins = [x.strip() for x in ins]
        outs =  [x.strip() for x in outs]
        cols.extend(ins)
        cols.extend(outs)
    cols = list(dict.fromkeys(cols)) # Just unique entries, preserving order
    # # Remove photons
    species = np.array([s for s in cols if 'hv' not in s])
    emissions = np.array([s for s in cols if 'hv' in s])
    emissions = np.concatenate([emissions, add_emissions])

    # Re-ordering for convenience
    # Take out the NO(v) and reorder them numerically
    species1 = [s for s in species if not s.startswith('NO(v')] # the non-NO(X,v) species
    species0 = [s for s in species if     s.startswith('NO(v')] # NO(v)
    species0 = [f'NO(v={i})' for i in range(len(species0))]
    species = np.concatenate([species0, species1])
    # Take out the N2(v) and reorder them numerically
    species1 = [s for s in species if not s.startswith('N2(v')] # the non-N2(X,v) species
    species0 = [s for s in species if     s.startswith('N2(v')] # N2(v)
    species0 = [f'N2(v={i})' for i in range(len(species0))]
    species = np.concatenate([species0, species1])


    N = len(species)
    Ne = len(emissions)
    M = len(df)
    dfin  = pd.DataFrame(index=df['Reaction'], columns=species, data=np.zeros((M,N),dtype=int) )
    dfout = pd.DataFrame(index=df['Reaction'], columns=species, data=np.zeros((M,N),dtype=int) )
    dfemis= pd.DataFrame(index=df['Reaction'], columns=emissions, data=np.zeros((M,Ne),dtype=int) )
    for i in df.index:
        s = df.loc[i, 'Reaction']
        inout = s.split(' → ')
        ins = inout[0].split(' + ')
        outs = inout[1].split(' + ')
        ins = [x.strip() for x in ins]
        outs =  [x.strip() for x in outs]
        for x in ins:
            dfin.loc[s, x] += 1
        for x in outs:
            if x in species:
                dfout.loc[s, x] += 1
            if x in emissions:
                dfemis.loc[s,x] += 1

    # This code added after I created the above DataFrames, to be converted back to dicts, 
    # because I found that easier. I'm keeping it inelegant like this in case I ever want
    # to use DataFrames again
    # Convert from DataFrame to dict, since that simplifies things.
    # Each (key,value) pair is (reaction, list of relevant reactants or products or emissions)
    din = {}
    dout = {}
    demis = {}
    for rx in dfin.index:
        # This accounts for possibly the same reactant appearing twice (or product)
        din[rx]   = [sp for sp, n in dfin.loc[rx].items()  for _ in range(n) if n > 0]
        dout[rx]  = [sp for sp, n in dfout.loc[rx].items() for _ in range(n) if n > 0]
        demis[rx] = [sp for sp, n in dfemis.loc[rx].items() for _ in range(n) if n > 0]
        
        
    # Convert din, dout, demis to species indices instead of species string keys
    reactions = list(din.keys())
    nin = {}
    nout = {}
    nemis = {}
    for rx in reactions:
        xin = []
        xout = []
        xemis = []
        for s in din[rx]:
            j, = np.where(species==s)
            xin.append(j[0])
        for s in dout[rx]:
            j, = np.where(species==s)
            xout.append(j[0])
        for s in demis[rx]:
            j, = np.where(emissions==s)
            xemis.append(j[0])
        nin[rx] = np.array(xin)
        nout[rx] = np.array(xout)
        nemis[rx] = np.array(xemis)
        
    # Dictionary to convert species name to index
    sidx = {}
    for s in species:
        sidx[s] = np.where(species==s)[0][0]
        
    # Dictionary to convert emission name to index
    eidx = {}
    for e in emissions:
        eidx[e] = np.where(emissions==e)[0][0]
        
    # Be sure to include alternative versions of species names, for flexibility
    if 'N2(v=0)' in species:
        sidx['N2'] = sidx['N2(v=0)']
    if 'NO(v=0)' in species:
        sidx['NO'] = sidx['NO(v=0)']
    if 'O' in species:
        sidx['O(3P)'] = sidx['O']
    if 'N' in species:
        sidx['N(4S)'] = sidx['N']

    # Check for common mistakes
    if 'N2' in species and 'N2(v=0)' in species:
        v = 'N2'
        print(f'\nREACTIONS WITH {v}\n')
        [print(r) for r in din if v in din[r]]
        [print(r) for r in dout if v in dout[r]]
        v = 'N2(v=0)'
        print(f'\nREACTIONS WITH {v}\n')
        [print(r) for r in din if v in din[r]]
        [print(r) for r in dout if v in dout[r]]
        raise Exception("N2 and N2(v=0) shouldn't both be included")
    if 'NO' in species and 'NO(v=0)' in species:
        v = 'NO'
        print(f'\nREACTIONS WITH {v}\n')
        [print(r) for r in din if v in din[r]]
        [print(r) for r in dout if v in dout[r]]
        v = 'NO(v=0)'
        print(f'\nREACTIONS WITH {v}\n')
        [print(r) for r in din if v in din[r]]
        [print(r) for r in dout if v in dout[r]]
        raise Exception("NO and NO(v=0) shouldn't both be included")
    
                
    return species, emissions, din, dout, demis, nin, nout, nemis, sidx, eidx



def eq_dens(s, n, prodrate, drc, nin, nout, species):
    '''
    Compute equilibrium concentration for species s using densities and reaction rates given. Intended
    to be used to compute minor species concentrations.
    
    This was strongly modified from the 2020 version. Production rate is calculated outside module.
    
    s - str - species to compute (e.g., 'NO', 'O+(2D)')
    n - (Ns) array - densities
    prodrate - cm^-3 s^-1. Total production rate at this timestep
    drc - dictionary of reaction rate coefficients, already evaluated as scalars, not formulas
    species - list of str - the list of species names
    '''
    reactions = list(drc.keys())
    Ns = np.shape(n)
    js = np.where(species==s)[0][0]

    lossrate = 0.0 # 1/s
    for jr, rx in enumerate(reactions): # loop over reactions
        # Find product of input species densities and multiply by reaction coefficient
        if js in nout[rx] and js in nin[rx]: # does not affect equilibrium: exclude
            continue
        if js in nin[rx]: # It's a loss rate
            # Note: excluding the species under consideration from the loss rate, since it's 
            # the unknown we're solving for
            lossrate += drc[rx] * np.array([n[jx] for jx in nin[rx] if jx!=js]).prod(axis=0) 
    neq = prodrate/lossrate
    return neq
    


